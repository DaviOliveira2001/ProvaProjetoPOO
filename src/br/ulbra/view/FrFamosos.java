    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int i = 0;
        if(JOptionPane.showConfirmDialog(null,"Tem certeza de que deseja realizar esta ação?","EXCLUIR",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
        JOptionPane.showConfirmDialog(null, "Última chance. Deseja mesmo continuar?");
        
            f.listaFamosos.clear();
            f.listaFamososAlfabetica.clear();
            JOptionPane.showMessageDialog(null, "A lista foi deletada.");
            txtNome.requestFocus();
    }else{
            JOptionPane.showMessageDialog(null, "Exclusão cancelada.");
            txtNome.requestFocus();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

