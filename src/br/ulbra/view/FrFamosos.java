    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(txtNome.getText().length() > 0){
            f.save(txtNome.getText());
            txtLista.setText(getList());
            txtNome.setText(null);
            txtNome.requestFocus();
        }else{
            JOptionPane.showMessageDialog(null, "Digite um nome.");
            txtNome.requestFocus();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
        String nome = JOptionPane.showInputDialog("Informe o nome do artista.");
        txtPesquisar.setText(f.getNome(f.search(nome)));
        
        txtNome.requestFocus();
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        if(JOptionPane.showConfirmDialog(null, "Tem certeza?", "EXLUIR", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
        f.delete(f.search(txtExcluir.getText())); 
        JOptionPane.showMessageDialog(null, "Exclusão completa.");
        txtNome.requestFocus();
        }else{
            JOptionPane.showMessageDialog(null, "Exclusão cancelada.");
            txtNome.requestFocus();
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        int i = 0;
        if(i >= 0){
        f.update(f.search(txtEditar.getText()), JOptionPane.showInputDialog("Digite o novo nome."));
        txtNome.requestFocus();
        }else{
            JOptionPane.showMessageDialog(null, "Digite um nome para ser editado.");
            txtNome.requestFocus();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

